package javabasics;

public class Assignment3 {

	static void add() {
		int a=10;
		int b=20;
		int c=a+b;
		System.out.println(c);
	}
	
	static void sub() {
		int a=10;
		int b=20;
		int c=b-a;
		System.out.println(c);
	}
	
	static void multi() {
		int a=10;
		int b=20;
		int c=a*b;
		System.out.println(c);
	}
	
	static void division() {
		int a=10;
		int b=20;
		int c=b/a;
		System.out.println(c);
	}
	public static void main(String[] args) {
		
		
		add();
		sub();
		multi();
		division();

	}
}
